"""
ASSIGNMENT OPERATOR
          A=5
          A=A+2  (OR) A+=2
"""

number1=input("Enter num1 : ")
number2=input("Enter num2 : ")
n1=int(number1)
n2=int(number2)
n1=n1+2
n2=n2+3
print(n1)
print(n2)
n1+=2
print(n1)
n2+=2
print(n2)
n1-=2
n2-=2
print(n1)
print(n2)
n1*=2
n2*=2
print(n1)
print(n2)

